// Program for Relational Operators

#include<stdio.h>
 
int main()
{
    int a,b;
    a=10;
    b=20;
    printf("%d\n",a<b);     // for a<b
    printf("%d\n",a<=b);    // for a<=b
    printf("%d\n",a>b);     // for a>b
    printf("%d\n",a>=b);    // for a>=b
    printf("%d\n",a==b);    // for a==b
    printf("%d\n",a!=b);    // for a!=b
    
    return 0;
}